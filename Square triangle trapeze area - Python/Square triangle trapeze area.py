
a = float(input("Enter A value: "))
b = float(input("Enter B value: "))
c = float(input("Enter C value: "))

squareare = pow(a, 2)
trianglearea = (a * b) / 2
trapezearea = ((a + b) * c) / 2

print(f"SQUARE AREA = {squareare:.4f}")
print(f"TRIANGLE AREA = {trianglearea:.4f}")
print(f"TRAPEZE AREA = {trapezearea:.4f}")
